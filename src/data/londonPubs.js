  // Helper function to get random price within reasonable range
  export const getRandomPrice = (min = 3.45, max = 4.50) => {
    return Number((Math.random() * (max - min) + min).toFixed(2));
  };
  
  // Helper function to get random rating
  export const getRandomRating = (min = 3.9, max = 4.7) => {
    return Number((Math.random() * (max - min) + min).toFixed(1));
  };
  
  // Helper function to get random ratings count
  export const getRandomRatingsCount = (min = 100, max = 300) => {
    return Math.floor(Math.random() * (max - min + 1) + min);
  };
  
  // Function to generate a new pub entry
  export const generatePubData = (name, address, latitude, longitude) => {
    const drinks = {};
    // Randomly select 5-8 drinks
    const numDrinks = Math.floor(Math.random() * 4) + 5;
    const selectedDrinks = [...COMMON_DRINKS.beers]
      .sort(() => 0.5 - Math.random())
      .slice(0, numDrinks);
  
    selectedDrinks.forEach(drink => {
      drinks[drink] = {
        price: getRandomPrice(),
        rating: getRandomRating(),
        ratings_count: getRandomRatingsCount()
      };
    });
  
    return {
      id: Date.now(),
      name,
      address,
      latitude,
      longitude,
      drinks
    };
  };